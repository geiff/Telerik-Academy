/*Write a program that prints at the console the numbers from 1 to 1000, each at a separate line.*/

using System;

namespace Problem17.PlayWithVSDebugger
{
    class PlayWithVSDebugger
    {
        static void Main()
        {
            for (int i = 1; i <= 1000; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}